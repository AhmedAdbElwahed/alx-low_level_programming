#include "3-calc.h"
/**
 * op_add - a function that add to numbers.
 * @a: integer number.
 * @b: integer number.
 * Return: the sum of two numbers.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that subtract to numbers.
 * @a: integer number.
 * @b: integer number.
 * Return: the subtraction of two numbers.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiply to numbers.
 * @a: integer number.
 * @b: integer number.
 * Return: the multiplication of two numbers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divide to numbers.
 * @a: integer number.
 * @b: integer number.
 * Return: the division of two numbers.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that mod to numbers.
 * @a: integer number.
 * @b: integer number.
 * Return: the modules of two numbers.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

